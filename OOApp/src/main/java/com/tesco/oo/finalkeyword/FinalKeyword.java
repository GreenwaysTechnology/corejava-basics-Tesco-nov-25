package com.tesco.oo.finalkeyword;

public class FinalKeyword {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("A" + a);
        a = 90;
        System.out.println("A" + a);

        final float PI = 3.14f;
        System.out.println(PI);
        //PI =90.99f;

    }
}
